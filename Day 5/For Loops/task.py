# Create a list of fruits containing three string elements
fruits = ["Apple", "Peach", "Pear"]

# Loop through each fruit in the fruits list
# 'fruit' is the iterator variable that takes the value of each element in the list
for fruit in fruits:
    # Print the current fruit name
    print(fruit)
    # Print the fruit name concatenated with " pie"
    print(fruit + " pie")

# Print the entire fruits list to show the original list remains unchanged
print(fruits)